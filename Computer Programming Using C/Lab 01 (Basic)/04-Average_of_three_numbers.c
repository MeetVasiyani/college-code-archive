//Print average of three numbers. (with & without scanf) 
#include <stdio.h>
void main(){
	int a,b,c;
	printf("Enter first Number : ");
	scanf("%d",&a);
	printf("Enter second Number : ");
	scanf("%d",&b);
	printf("Enter third Number : ");
	scanf("%d",&c);
	printf("Average of Three Numbers is = %d",(a+b+c)/3);
	
}

